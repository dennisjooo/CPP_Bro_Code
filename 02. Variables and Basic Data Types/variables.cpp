#include <iostream>
int main(){

    // Integers
    int x = 5; //declareation and assignment
    int y = 6;
    int z = x + y;
    std::cout << "The value of z is: " << z << std::endl;

    // Floating point numbers(Doubles)
    double gpa = 3.78;
    std::cout << "The value of gpa is: " << gpa << std::endl;

    // Characters
    char grade = 'A';
    std::cout << "The value of grade is: " << grade << std::endl;

    // Boolean
    bool isMale = true;
    std::cout << "The value of isMale is: " << isMale << std::endl;

    // Strings
    std::string phrase = "Hello World";
    std::cout << "The value of phrase is: " << phrase << std::endl;

}
